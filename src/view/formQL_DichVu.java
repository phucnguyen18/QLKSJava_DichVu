    private void Bt_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_XoaActionPerformed
        // TODO add your handling code here:
        
      int row = Tb_DichVu.getSelectedRow();
        if (row != -1) 
        {
            String iddv = Tb_DichVu.getValueAt(row, 0).toString();
            int output = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa nhân viên " + iddv + "?", "Xóa nhân viên", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);//, icon.iconDelete());
            if (output == JOptionPane.YES_OPTION) 
           
            {
                if (dv.deleteDv(iddv)) 
                {
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Xóa", JOptionPane.INFORMATION_MESSAGE);
                    loadTableDV(dv.getListDV());
                  
                } 
                else 
                {
                    JOptionPane.showMessageDialog(this, "Chưa xóa được", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }

        } 
        else 
        {
            JOptionPane.showMessageDialog(this, "Chưa chọn mục nào!", "Trống", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_Bt_XoaActionPerformed

