    public void loadTableDV(ArrayList<dichvuControctor> listDV) 
    {
        DefaultTableModel model = (DefaultTableModel) Tb_DichVu.getModel();
        model.setRowCount(0);
        for (int i = 0; i < listDV.size(); i++) {
            model.addRow(new Object[]
            {
                listDV.get(i).getId(),
                listDV.get(i).getTendv(),
                listDV.get(i).getGiadv(),
                
            });
        }
        Tb_DichVu.setModel(model);
    }
    
    
    public void timDV() 
    {
        String key = TF_Search.getText();
        
        listDV = dv.searchDV(key);
        if (listDV.size() != 0) 
        {
            loadTableDV(listDV);
            checkSearch = true;
        } 
        else 
        {
            JOptionPane.showMessageDialog(this, "Không có kết quả phù hợp", "Tìm kiếm", JOptionPane.INFORMATION_MESSAGE);            
            checkSearch = false;
        }
    }
    
   

